#include <string.h>

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t				i;
	unsigned const char	*str1;
	unsigned const char	*str2;

	i = 0;
	str1 = s1;
	str2 = s2;
	while (i < n)
	{
		if (str1[i] == str2[i])
			i++;
		else
			return (((unsigned char)str1[i]) - ((unsigned char)str2[i]));
	}
	return (0);
}
